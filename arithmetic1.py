# WAP to understand arithmetic operators

n1 = 11
n2 = 3

# print("Multiplication of ",n1,",",n2, "=",(n1*n2))
# Formatted Strings
print(f'Multiplication of {n1}, {n2} = {n1*n2}')

print(f'Division of {n1}, {n2} = {n1/n2}')
print(f'Modulus -> Remainder of {n1}, {n2} = {n1%n2}')
print(f'Integer Division of {n1}, {n2} = {n1//n2}')

print(f'Power {n1} raise to {n2} = {n1 ** n2}')
print(f'Addition of {n1}, {n2} = {n1+n2}')
print(f'Subtraction of {n1}, {n2} = {n1-n2}')